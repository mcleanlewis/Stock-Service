package Beans;

import java.net.Socket;

public class SocketWrapper {

	private Socket	socket;
	private String	token;

	public SocketWrapper(Socket socket, String token) {
		this.socket = socket;
		this.token = token;
	}

	/**
	 * @return the socket
	 */
	public Socket getSocket() {
		return socket;
	}

	/**
	 * @param socket
	 *            the socket to set
	 */
	public void setSocket(Socket socket) {
		this.socket = socket;
	}

	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}

	/**
	 * @param token
	 *            the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}

}
